            <li><strong>Contact Data:</strong> Email address and telephone numbers</li>
            <li><strong>Profile Data:</strong> Username, password (encrypted), profile picture, and identity verification documents (for riders)</li>
            <li><strong>Location Data:</strong> Pickup and delivery addresses, current location when using the app</li>
            <li><strong>Payment Information:</strong> Payment card details (processed securely by Stripe - see section 4)</li>
            <li><strong>Photos:</strong> Package photos for documentation purposes (via camera or photo library)</li>
